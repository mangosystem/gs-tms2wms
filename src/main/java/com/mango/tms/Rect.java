package com.mango.tms;

public class Rect {
	private int fX = 0;
	private int fY = 0;
	private int fWidth = 0;
	private int fHeight = 0;

	public Rect(int x, int y, int width, int height) {
		fX = x;
		fY = y;
		fWidth = width;
		fHeight = height;
	}

	public int getX() {
		return fX;
	}

	public void setX(int x) {
		fX = x;
	}

	public int getY() {
		return fY;
	}

	public void setY(int y) {
		fY = y;
	}

	public int getWidth() {
		return fWidth;
	}

	public void setWidth(int width) {
		fWidth = width;
	}

	public int getHeight() {
		return fHeight;
	}

	public void setHeight(int height) {
		fHeight = height;
	}

	public String toString() {
		StringBuffer sb = new StringBuffer();
		sb.append(fX).append(',');
		sb.append(fY).append(',');
		sb.append(fWidth).append(',');
		sb.append(fHeight);
		return sb.toString();
	}

}
